                    ViewData["_table_name"] = _table_name;
                    ViewData["_table_title"] = _table_title;
                    ViewData["pkKey"] = FreeManHoursModel._pkKey;
                    ViewData["displayItem"] = FreeManHoursModel.GetDisplayItem();
                    ViewData["column_att"] = FreeManHoursModel.GetGridColumn();
                    ViewData["param"] = FreeManHoursModel.GetListParam();
                    ViewData["UrlGetList"] = baseUrl + _path_controler + "GetListData";
