        [HttpPost]
		[Obsolete]
        public JsonResult Update(HealthGangguFisikModel.ViewModel fieldModel)
        {
            ProsesResult result = new ProsesResult();
            if (SecurityHelper.onPageInit(HttpContext))
            {
                if (SecurityHelper.HasRule(HttpContext, _rule_edit))
                {
                    string id_old = fieldModel.id_old;
                    var PkValue = new OrderedDictionary();
                    PkValue["id"] = id_old;
                    OrderedDictionary data_old = DataModel.DtToDictionary(HealthGangguFisikModel.GetData(PkValue));

                    OrderedDictionary data = DataModel.DtToDictionary(HealthGangguFisikModel.GetData(PkValue));
                    data["id"] = fieldModel.id;
                    data["health_id"] = fieldModel.health_id;
                    data["gangguan_fisik_id"] = fieldModel.gangguan_fisik_id;
                    data["gcu_result_id"] = fieldModel.gcu_result_id;
                    data["deskripsi"] = AntiXss.HtmlEncode(fieldModel.deskripsi);
                    SqlHelper.ConvertEmptyValuesToDBNull(data);
                    SqlHelper.ConvertEmptyValuesToDBNull(data_old);
                    if (DataModel.HasUpdate(data, data_old))
                    {
                        data["update_by"] = SecurityHelper.CurrentUserId(HttpContext);
                        data["update_at"] = DateTime.Now;
                        result = HealthGangguFisikModel.Update(_hostingEnvironment, HttpContext, data, data_old);
                    }
                    else
                    {
                        result.status = 2;
                        result.title = ResxHelper.GetValue("Message", "ErrorMessage");
                        result.message = ResxHelper.GetValue("Message", "NoRecodeUpdate");
                    }
                    return Json(result);
                }
                else
                {
                    result.status = 3;
                    result.title = ResxHelper.GetValue("Message", "ErrorMessage");
                    result.message = ResxHelper.GetValue("Message", "NotAuthorization");
                    return Json(result);
                }
            }
            else
            {
                result.status = 3;
                result.title = ResxHelper.GetValue("Message", "ErrorMessage");
