        public JsonResult Update(AccidentFollowupModel.ViewModel fieldModel)
        {
            ProsesResult result = new ProsesResult();
            if (SecurityHelper.onPageInit(HttpContext))
            {
                if (SecurityHelper.HasRule(HttpContext, _rule_edit))
                {
                    string id_old = fieldModel.id_old;
                    var PkValue = new OrderedDictionary();
                    PkValue["id"] = id_old;
                    OrderedDictionary data_old = DataModel.DtToDictionary(AccidentFollowupModel.GetData(PkValue));
                    data_old["due_date"] = data_old["due_date"].ToString() != "" ? String.Format("{0:yyyy-MM-dd}", data_old["due_date"]) : "";
                    OrderedDictionary data = DataModel.DtToDictionary(AccidentFollowupModel.GetData(PkValue));
                    data["id"] = fieldModel.id;
                    data["acc_id"] = fieldModel.acc_id;
                    data["followup_accident_id"] = fieldModel.followup_accident_id;
                    data["deskripsi_tindak_lanjut"] = AntiXss.HtmlEncode(fieldModel.deskripsi_tindak_lanjut);
                    data["due_date"] = fieldModel.due_date;
                    data["status_followup_id"] = fieldModel.status_followup_id;
                    SqlHelper.ConvertEmptyValuesToDBNull(data);
                    SqlHelper.ConvertEmptyValuesToDBNull(data_old);
                    if (DataModel.HasUpdate(data, data_old))
                    {
                        data["update_by"] = SecurityHelper.CurrentUserId(HttpContext);
                        data["update_at"] = DateTime.Now;
                        result = AccidentFollowupModel.Update(_hostingEnvironment, HttpContext, data, data_old);
                    }
                    else
                    {
                        result.status = 2;
                        result.title = ResxHelper.GetValue("Message", "ErrorMessage");
                        result.message = ResxHelper.GetValue("Message", "NoRecodeUpdate");
                    }
                    return Json(result);
                }
                else
                {
                    result.status = 3;
                    result.title = ResxHelper.GetValue("Message", "ErrorMessage");
                    result.message = ResxHelper.GetValue("Message", "NotAuthorization");
                    return Json(result);
